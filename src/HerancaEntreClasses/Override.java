package HerancaEntreClasses;

public class Override {
    /*
    A anotation Override é muito importante:

    - mostra que voce está sobrescrevendo um metodo de uma superclasse;
    - não é obrigatório;
    - Ele faz com que você evite sobrescrever um metodo com parametros de tipos diferentes
    te avisando que o código não pode ser compilado. Sem ele o código rodaria com erros
    concequentes dessa ação incorreta;
    - Ex: se na classe pai um método recebe o parâmetro int e na subclasse o método recebe um double
    o compilador vai entender que seria um polimorfismo de sobrecarga e não de sobrescrita, causando, 
    em alguns casos, erros de compilação. Se estiver escrito override isso não acontece pois ele te avisa.
    - Ele te ajuda a mostrar possiveis erros na hora de sobrescrever;
    - SEMPRE USE ELE!
     */

}
